/**
 * In factory design pattern we face some problems such as
 * 1) when we have to pass many arguments to create object
 * 2) some parameters might be optional
 * 3) all responsibility to create object belongs to factory thus
 *   if object is heavy then all complexity will belong to factory class
 *
 *  Builder pattern elevate the problems in factory.
 *  Here we build the objects step by step.
 *  And finally we return the object with desired values.
 *
 *  Builder pattern mainly used to create immutable objects
 */
